package org.nixos.disnix.example.webservices;

public class Room
{
	private String room;
	
	private String zipcode;
	
	public Room()
	{		
	}

	public String getRoom()
	{
		return room;
	}

	public void setRoom(String room)
	{
		this.room = room;
	}

	public String getZipcode()
	{
		return zipcode;
	}

	public void setZipcode(String zipcode)
	{
		this.zipcode = zipcode;
	}
}
